    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        userDAO dao = new userDAO();
        String email = request.getParameter("email");
        HttpSession session = request.getSession();
        model.User a = dao.checkAcc(email);

        if (a != null) {
            SendEmailCode sm = new SendEmailCode();
            String code = sm.getRandom();
            UserC userc = new UserC(code, email);
            boolean test = sm.sendEmail2(userc);
            if (test && userc != null) {
                // Instead of changing the password here, just redirect to another page
                session.setAttribute("email", email);
                session.setAttribute("code", code);
                response.sendRedirect("resetPassword.jsp");
            }
        } else {
            String msg = "Email không tồn tại!";
            session.setAttribute("msg_err", msg);
            response.sendRedirect("forgotpass.jsp");
        }
    }

